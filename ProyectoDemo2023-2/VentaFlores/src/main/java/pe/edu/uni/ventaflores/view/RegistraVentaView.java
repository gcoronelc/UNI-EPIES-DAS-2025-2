    private void btnAgregarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarItemActionPerformed
        // Probando el dialogo
        NuevoItemView view = new NuevoItemView(null, true);
        view.setVisible(true);
        
        
        // Recoge datos
        DetalleVenta dv = new DetalleVenta();
        dv.setIdproducto(Integer.parseInt(txtCodigo.getText()));
        dv.setCantidad(Integer.parseInt(txtCantidad.getText()));
        listaDetalle.add(dv);
        // Actualizar detalle
        DefaultTableModel tabla = (DefaultTableModel) tblDetalle.getModel();
        tabla.setRowCount(0);
        for (DetalleVenta d : listaDetalle) {
            Object[] rowData = {d.getIdproducto(), d.getNombre(),
                d.getPreventa(), d.getCantidad(), d.getSubtotal()};
            tabla.addRow(rowData);
        }
    }//GEN-LAST:event_btnAgregarItemActionPerformed

    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        // Datos
        Venta venta = new Venta();
        venta.setIdcliente(Integer.parseInt(txtCliente.getText()));
        venta.setIdestado(1);
        venta.setVendedor(3);
        venta.setDetalle(listaDetalle);
        // Proceso
        try {
            VentaController controller = new VentaController();
            venta = controller.procesarVenta(venta);
            JOptionPane.showMessageDialog(null, "IdVenta=" + venta.getIdventa());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnProcesarActionPerformed


